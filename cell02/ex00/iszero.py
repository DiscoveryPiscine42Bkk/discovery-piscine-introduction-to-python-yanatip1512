# รับค่าจาก terminal แล้วทำการแปลงข้อมูลจาก ตัวอักษร -> ตัวเลข จากนั้นทำการเก็บไว้ในตัวแปรชื่อว่า value
value = int(input())

# ทำการเช็คว่าค่าตัวเลขที่รับเข้ามามีค่าเท่ากับ 0 หรือไม่
# Note: ถ้าต้องการจะเทียบว่าค่าสองค่ามีค่าเท่ากัน ต้องใช้เครื่องหมาย == เท่านั้น เพราะเครื่องหมาย = เพียงตัวเดียวหมายถึงการใส่ค่าเข้าตัวแปร
if value == 0:
	print("This number is equal to zero")

# หากค่าที่รับมาไม่ใช่ 0: ให้ทำงานตรงนี้แทน
else:
	print("This number is different from zero")