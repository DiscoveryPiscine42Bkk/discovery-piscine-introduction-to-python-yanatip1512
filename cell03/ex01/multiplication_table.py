# รับค่าตัวคูณจาก terminal แล้วทำการแปลงข้อมูลจาก ตัวอักษร -> ตัวเลข จากนั้นทำการเก็บไว้ในตัวแปรชื่อว่า value
value = int(input("Enter a number "))

# ทำการวนซ้ำไปเรื่อยๆ 10 ครั้ง โดยในการวนซ้ำแต่่ละครั้งจะทำการแสดงผลในรูปแบบของ
# "{สูตรคูณแม่ปัจจุบัน} x {ตัวคูณที่ใส่เข้ามา} = {สูตรคูณแม่ปัจจุบัน คูณกับ ตัวคูณที่ใส่เข้ามา}"
for num in range(10):
	print(f"{num} x {value} = {num * value}")